  class CalculadoraClas {
      companion object {
          fun resultSoma(num1: Int, num2: Int): Int {
              val total = num1 + num2
              return total
          }

          fun Resultsub(num1: Int, num2: Int): Int {
              val total = num1 - num2
              return total
          }

          fun Resultm(num1: Int, num2: Int): Int {
              val total = num1 * num2
              return total
          }

          fun ResultD(num1: Int, num2: Int): Int {
              val total = num1 / num2
              return total
          }

      }
}